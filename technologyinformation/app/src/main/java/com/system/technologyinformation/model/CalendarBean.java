package com.system.technologyinformation.model;

public class CalendarBean {
    private int calendarId;
    private String calendarContent;
    private int calendarNumber;

    public int getCalendarId() {
        return calendarId;
    }

    public void setCalendarId(int calendarId) {
        this.calendarId = calendarId;
    }

    public String getCalendarContent() {
        return calendarContent;
    }

    public void setCalendarContent(String calendarContent) {
        this.calendarContent = calendarContent;
    }

    public int getCalendarNumber() {
        return calendarNumber;
    }

    public void setCalendarNumber(int calendarNumber) {
        this.calendarNumber = calendarNumber;
    }
}
